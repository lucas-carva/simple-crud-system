    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed

        String sql;
        String Codigo;
        Codigo = tfCodigo.getText();
        try{
            sql = "select * fromfuncionarios where idFuncionario = " + Codigo;

            Statement stm = con.createStatement();

            ResultSet rs = stm.executeQuery(sql);
            rs.first();

            tfCodigo.setText(rs.getString("idFuncionario"));
            tfNome.setText(rs.getString("nome"));
            tfNumero.setText(rs.getString("numero"));
            tfCep.setText(rs.getString("cep"));
            tfCpf.setText(rs.getString("cpf"));
            tfEmail.setText(rs.getString("email"));
            tfCargo.setText(rs.getString("cargo"));
            tfSalario.setText(rs.getString("salario"));


            
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Coloque outro código do cliente e tente novamente","ERRO",JOptionPane.INFORMATION_MESSAGE);

        }         // TODO add your handling code here:
    }//GEN-LAST:event_btnConsultarActionPerformed

