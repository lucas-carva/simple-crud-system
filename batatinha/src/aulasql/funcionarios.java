    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        String sql;
        String Codigo;
        int opc;
        Codigo = tfCodigo.getText();

        try{
            opc = JOptionPane.showConfirmDialog(null, "Confirma exclusão", "Excluindo...", 0);
            if (opc == 0){
                sql="DELETE FROM funcionario where idFuncionario ="+ Codigo ;

                Statement stm = con.prepareStatement(sql);
                stm.execute(sql);

                tfCodigo.setText("");
                tfNome.setText("");
                tfNumero.setText("");
                tfCep.setText("");
                tfCpf.setText("");
                tfEmail.setText("");

                JOptionPane.showMessageDialog(null, "Sucesso ao excluir os dados","Inclusão",JOptionPane.INFORMATION_MESSAGE);
                tfCodigo.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "Cancelamento na operação!","Cancelado",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "coloque o codigo do fcunionário e tente novamente" + e,"ERRO",JOptionPane.INFORMATION_MESSAGE);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_btnExcluirActionPerformed

