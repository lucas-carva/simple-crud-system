    private void btnConsultarEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarEmpActionPerformed
        // TODO add your handling code here:]

        String sql;
        String idProduto;
        idProduto = tfCodigoProd.getText();
        try{
            sql = "select * from produto where idProduto = " + idProduto;

            Statement stm = con.createStatement();

            ResultSet rs = stm.executeQuery(sql);
            rs.first();

            tfCodigoProd.setText(rs.getString("idProduto"));
            tfTipoProduto.setText(rs.getString("nome"));
            tfLote.setText(rs.getString("lote"));
          
            fPreco.setText(rs.getString("preco"));
            
        }    
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "Coloque outro código do "
       + "fornecedor e tente novamente","ERRO",JOptionPane.INFORMATION_MESSAGE);

        }

    }//GEN-LAST:event_btnConsultarEmpActionPerformed

