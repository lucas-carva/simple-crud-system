    private void btnExcluirEmpActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirEmpActionPerformed
        // TODO add your handling code here:

        String sql;
        String IdProduto;
        int opc;
        IdProduto = tfCodigoProd.getText();

        try{
            opc = JOptionPane.showConfirmDialog(null, "Confirma exclusão", 
                 "Excluindo...", 0);
            if (opc == 0){
                sql="DELETE FROM produto where idProduto ="+ IdProduto;

                Statement stm = con.prepareStatement(sql);
                stm.execute(sql);

                tfCodigoProd.setText("");
                tfTipoProduto.setText("");
                tfLote.setText("");                
                fPreco.setText("");
                

                JOptionPane.showMessageDialog(null, "Sucesso ao excluir os "
                   + "dados","Inclusão",JOptionPane.INFORMATION_MESSAGE);

                tfCodigoProd.requestFocus();
            } else {
                JOptionPane.showMessageDialog(null, "cancelamento na operação!"
               ,"Cancelado",JOptionPane.INFORMATION_MESSAGE);
            }
        }
        catch(SQLException e){
            JOptionPane.showMessageDialog(null, "coloque o codigo do cliente e"
              + " tente novamente" + e,"ERRO",JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_btnExcluirEmpActionPerformed

